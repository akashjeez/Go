package main
import "fmt"
func main() {
	a, b := 60, 13
	fmt.Println(" Value of A and B :",a, b)
	fmt.Println(" Bitwise operator & for A and B :",a&b)
	fmt.Println(" Bitwise operator | for A and B :",a|b)
	fmt.Println(" Bitwise operator ^ for A and B :",a^b)
	fmt.Println(" Bitwise operator << for A by 2 :",a<<2)
	fmt.Println(" Bitwise operator >> for A by 2 :",a>>2)
}
